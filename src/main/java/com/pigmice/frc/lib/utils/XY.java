package com.pigmice.frc.lib.utils;

/**
 * 2D data type with x and y components.
 */
public interface XY {
    /**
     * Gets the x component of this 2D data type
     *
     * @return The x component
     */
    public double getX();

    /**
     * Gets the y component of this 2D data type
     *
     * @return The y component
     */
    public double getY();
}
